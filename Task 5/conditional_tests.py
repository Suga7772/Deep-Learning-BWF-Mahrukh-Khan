name = 'mahrukh'
# equality
print(name == 'andaleeb')
print(name == 'mahrukh')
# unequality
print(name != 'Susan')
print(name != 'mahrukh')

food = 'PIZZA'
# lower
print(food.lower() == 'pizza')
print(food.lower() == 'italian')

age = 20
# numerical
print(age > 20)
print(age > 2)
print(age >= 20)
print(age >= 19)
print(age < 19)
print(age < 25)
print(age <= 69)
print(age <= 7)

# and , or keyword
print(age > 12 and age < 25)
print(age > 26 and age < 20)
print(age > 19 or age < 100)
print(age > 26 or age < 8)

